#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	char	*dest;
	int		i;

	len = 0;
	while (src[len])
		len++;
	dest = (char *)malloc(len + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
